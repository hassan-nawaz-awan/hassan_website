'use client';

import { useEffect, useState } from 'react';
import { useTheme } from 'next-themes';
import { Moon, Sun } from 'lucide-react';

export default function ThemeToggle() {
  const { theme, setTheme } = useTheme();
  const [mounted, setMounted] = useState(false);

  useEffect(() => setMounted(true), []);

  if (!mounted) {
    return <div className="h-9 w-9" aria-hidden="true" />;
  }

  const isDark = theme === 'dark';

  return (
    <button
      type="button"
      onClick={() => setTheme(isDark ? 'light' : 'dark')}
      aria-label={isDark ? 'Switch to light mode' : 'Switch to dark mode'}
      className="flex h-9 w-9 items-center justify-center rounded-sm border border-hairline text-ink transition-colors hover:border-accent hover:text-accent dark:border-hairline-dark dark:text-ink-dark dark:hover:border-accent-dark dark:hover:text-accent-dark"
    >
      {isDark ? <Sun size={16} /> : <Moon size={16} />}
    </button>
  );
}
